public class ClassTypeJson {
    private String classTypeName;
    private int classTypeId;
    private String classTypeImgURL;
    private String classTypeLogoURL;

    public String getClassTypeName() {
        return classTypeName;
    }

    public void setClassTypeName(String classTypeName) {
        this.classTypeName = classTypeName;
    }

    public int getClassTypeId() {
        return classTypeId;
    }

    public void setClassTypeId(int classTypeId) {
        this.classTypeId = classTypeId;
    }

    public String getClassTypeImgURL() {
        return classTypeImgURL;
    }

    public void setClassTypeImgURL(String classTypeImgURL) {
        this.classTypeImgURL = classTypeImgURL;
    }

    public String getClassTypeLogoURL() {
        return classTypeLogoURL;
    }

    public void setClassTypeLogoURL(String classTypeLogoURL) {
        this.classTypeLogoURL = classTypeLogoURL;
    }
}
